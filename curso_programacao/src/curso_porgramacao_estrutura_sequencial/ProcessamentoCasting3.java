package curso_porgramacao_estrutura_sequencial;

public class ProcessamentoCasting3 {

	public static void main(String[] args) {
		
		double b, B, h, area;
		//area de trapezio
		b = 6.0; //base de cima
		B = 8.0; //base de baixo
		h = 5.0; //altura
		
		area = (b + B) / 2.0 * h;
		
		System.out.println(area);
	}

}
